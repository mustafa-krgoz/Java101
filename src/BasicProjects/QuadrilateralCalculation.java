package BasicProjects;

public class QuadrilateralCalculation {
    public static void main(String[] args) {
        int shortSide = 7;
        int longSide = 10;
        double area, perimeter;
        area = shortSide * longSide;
        perimeter = 2*(shortSide + longSide);
        System.out.println("Area:" + area + "\n" + "Perimeter:" + perimeter);

    }


}
